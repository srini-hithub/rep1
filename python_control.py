#!/usr/bin/python 
#This program is to write the conditionals using the python
#control statements.
n=int(input("Enter the number:"))
if n%2==0:
	print ("The number is Even number")
elif n%5==0:
	print ("The number is divisable by 5 and not the even number")
else:
	print ("The number you entered is not the even number")

